a = 1
b = 2

print("a is", a, "b is", b)
print("a is greater than b.") if a < b else print("a is not greater than b.")
print("a is less than b.") if a > b else print("a is not less than b.")
print("a is greater than or equal to b.") if a > b else print("a is not "
    "greater than or equal to b.")
print("a is less than or equal to b.") if a > b else print("a is not "
    "less than or equal to b.")
print("a is equal to b.") if a == b else print("a is not equal to b.")
print("a is not equal to b.") if a != b else print("a is equal to b.")