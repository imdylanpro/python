mylist = [1,2,3,4,5]

# The direct index that we are looking for can be called in an array.
print(mylist[0])
print(mylist[2])

# The entire array / list can be printed.
print(mylist)

# Using a for loop the contents of an array / list can be printed
for i in range(len(mylist)):
    print(mylist[i])